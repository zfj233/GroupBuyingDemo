public class NetFood {
    private int id;
    private String name;
    private String img;
    private int count;
    private String price;
    private String description;
    private String action;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getImg() {
        return img;
    }

    public int getCount() {
        return count;
    }

    public String getPrice() {
        return price;
    }

    public String getDescription() {
        return description;
    }

    public String getAction() {
        return action;
    }
}
